package com.emedinaa.java.entity;

/**
 * Overloading
 */
public class Sum {

    public int add(int n1, int n2){
        return n1+n2;
    }

    public int add(int n1, int n2,int n3){
        return n1+n2+n3;
    }

    /*int add(int n1, int n2,int n3){
        return n1+n2+n3;
    }*/

    int add(int n1, int n2,int n3, int n4){
        return n1+n2+n3+n4;
    }
}
